let temp = 40;

if (temp > 30)
{
    console.log("Вы можете надеть легкую одежду");
}
else if (temp > 20)
{
    console.log("Вы можете надеть теплую одежду");
}
else if (temp < 10)
{
    console.log("Вы можете надеть зимнюю одежду");
}
else
{
    console.log("Выбор одежды предоставляется вам самим");
}